    public void addData(File file){
        MCSLogger.trace();        
        try {
            ServerImpl.setdata(file.getCanonicalPath(), true);           
            dataTableModel.addRow(new Object[]{file.getName(),"??"});                
        } catch (Exception e) {            
           new ReportDialog(new javax.swing.JFrame(), true, e).setVisible(true);
        }                         
    }
    
    public void clearData(){   
        MCSLogger.trace();
        dataTableModel = new DefaultTableModel(new Object [][] {},
            new String [] {"File name","Table name"});             
        dataTable.setModel(dataTableModel);    
    }    
    
